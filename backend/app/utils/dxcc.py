def lookup_dxcc(call_sign: str) -> Optional[str]:
    """通过呼号前缀推断DXCC实体名称"""
    if not call_sign:
        return None

    cs = call_sign.strip().upper()

    # 处理斜杠前缀：b7/ba7abc → 先用 b7 匹配，不行再取 ba7abc
    if "/" in cs:
        parts = cs.split("/")
        # 先尝试用操作地前缀（斜杠前短前缀）匹配
        if parts[0] and len(parts[0]) <= 3:
            for pattern, entity in DXCC_PREFIXES:
                if re.match(pattern, parts[0]):
                    return entity
        # 再用呼号本体匹配
        cs = max(parts, key=len)

    for pattern, entity in DXCC_PREFIXES:
        if re.match(pattern, cs):
            return entity

    return "Unknown"
